#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list				*list;
	unsigned int		size;

	size = content_size;
	if (!(list = (t_list*)malloc(sizeof(t_list))))
		return (NULL);
	if (content != NULL)
	{
		if (!(list->content = malloc(content_size)))
		{
			free(list);
			return (NULL);
		}
		ft_memcpy(list->content, content, (ft_strlen((char*)content) + 1));
		list->content_size = size;
	}
	else
	{
		list->content = NULL;
		list->content_size = 0;
	}
	list->next = NULL;
	return (list);
}
