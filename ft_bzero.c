#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*strz;

	i = 0;
	strz = (unsigned char*)s;
	while (i < n)
	{
		strz[i] = '\0';
		i++;
	}
}
